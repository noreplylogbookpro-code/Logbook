function renderBlogsList(allBlogs) {
    if (allBlogs.length === 0) return;

    const featuredContainer = document.getElementById('featured-post-container');
    const recentContainer = document.getElementById('recent-posts-container');
    const gridContainer = document.getElementById('posts-grid');

    const featured = allBlogs[0];
    featuredContainer.innerHTML = `
                <section class="featured-post">
                    <div class="featured-image">
                        <img src="${escapeHtml(featured.imageUrl)}" alt="${escapeHtml(featured.title)}">
                    </div>
                    <div class="featured-content">
                        <div class="post-meta">
                            <span class="post-badge">${escapeHtml(featured.category)}</span>
                            <span>• ${escapeHtml(featured.date)}</span>
                        </div>
                        <h2>${escapeHtml(featured.title)}</h2>
                        <p>${escapeHtml(featured.excerpt)}</p>
                        <a class="btn-read-more" onclick="openArticle('${escapeHtml(featured.slug)}')">
                            Read Article <i class="fas fa-arrow-right"></i>
                        </a>
                    </div>
                </section>
            `;

    if (allBlogs.length > 1) {
        gridContainer.innerHTML = '';
        const recents = allBlogs.slice(1);
        recents.forEach(post => {
            const postCard = document.createElement('div');
            postCard.className = 'post-card';
            postCard.innerHTML = `
                        <div class="post-card-content">
                            <div class="post-meta">
                                <span class="post-badge" style="background:#e6fffa; color:#00a389;">${escapeHtml(post.category)}</span>
                                <span>• ${escapeHtml(post.date)}</span>
                            </div>
                            <h3>${escapeHtml(post.title)}</h3>
                            <p>${escapeHtml(post.excerpt)}</p>
                            <a class="btn-text" onclick="openArticle('${escapeHtml(post.slug)}')">
                                Read more <i class="fas fa-arrow-right"></i>
                            </a>
                        </div>
                    `;
            gridContainer.appendChild(postCard);
        });
        recentContainer.classList.remove('hidden');
    } else {
        recentContainer.classList.add('hidden');
    }
}

function escapeHtml(str) {
    return String(str).replace(/[&<>"']/g, function (m) {
        if (m === '&') return '&amp;';
        if (m === '<') return '&lt;';
        if (m === '>') return '&gt;';
        if (m === '"') return '&quot;';
        if (m === "'") return '&#39;';
        return m;
    });
}

function showToast(msg) {
    let t = document.querySelector('.toast-message');
    if (t) t.remove();
    t = document.createElement('div');
    t.className = 'toast-message';
    t.style.background = '#10b981'; // emerald green
    t.innerHTML = `<i class="fas fa-check-circle"></i> ${msg}`;
    document.body.appendChild(t);
    setTimeout(() => t.remove(), 3000);
}

function openArticle(slug) {
    const article = articles[slug];
    if (!article) return;

    document.getElementById('modalTitle').innerText = article.title;
    document.getElementById('modalDate').innerText = `• ${article.date}`;
    document.getElementById('modalCategory').innerText = article.category;
    document.getElementById('modalBody').innerHTML = article.content;

    const bloggerLink = document.getElementById('bloggerLink');
    if (article.url && article.url !== '#') {
        bloggerLink.href = article.url;
        bloggerLink.style.display = '';
    } else {
        bloggerLink.style.display = 'none';
    }

    const overlay = document.getElementById('articleReaderOverlay');
    overlay.classList.add('active');
    document.body.style.overflow = 'hidden'; // prevent scrolling behind modal
}

function closeArticle() {
    const overlay = document.getElementById('articleReaderOverlay');
    overlay.classList.remove('active');
    document.body.style.overflow = ''; // restore scrolling
}

function handleOverlayClick(e) {
    if (e.target.id === 'articleReaderOverlay') {
        closeArticle();
    }
}
